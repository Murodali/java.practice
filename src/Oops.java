public class Oops {
    public static void main(String[] args) {
        int x = 0;
        System.out.printf("x is %d\n",x);

        double a = 15.2f;
        System.out.printf("x is now %.1f\n",a);

        int y;
        int i = (int)a;
        y = i + 1;
        System.out.printf("x and y are now %d and %d\n ",i,y);

    }
}

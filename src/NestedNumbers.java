public class NestedNumbers {
    public static void main(String[] args) {
        int rows = 3;
        int cols = 3;
        int i = 1;
        int j,o,t,tr,fr,fv,s,sv,e,ni;
        int n= 0,one=1,two=2,three=3,four=4,five=5,siz=6,seve=7,eight=8,nine=9;

        while(i<=rows){
            j=1;
            o=1;
            t=1;
            tr=1;
            fr=1;
            fv=1;
            s=1;
            sv=1;
            e=1;
            ni=1;

            while(j<=cols){
                System.out.print(n);

                j++;

            }
            while (o<=cols){
                System.out.print(one);
                o++;
            }
            while (t<=cols){
                System.out.print(two);
                t++;
            }
            while (tr<=cols){
                System.out.print(three);
                tr++;
            }
            while (fr<=cols){
                System.out.print(four);
                fr++;
            }
            while (fv<=cols){
                System.out.print(five);
                fv++;
            }
            while (s<=cols){
                System.out.print(siz);
                s++;
            }
            while (sv<=cols){
                System.out.print(seve);
                sv++;
            }
            while (e<=cols){
                System.out.print(eight);
                e++;
            }
            while (ni<=cols){
                System.out.print(nine);
                ni++;
            }

            i++;
            System.out.println();
        }

    }

}
